class OperadoresRelacionales {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        // Operadores relacionales
        System.out.println("a < b: " + (a < b));
        System.out.println("a > b: " + (a > b));
        System.out.println("a <= b: " + (a <= b));
        System.out.println("a >= b: " + (a >= b));

        // Operadores de igualdad
        System.out.println("a == b: " + (a == b));
        System.out.println("a != b: " + (a != b));
    }
}